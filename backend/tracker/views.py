# tracker/views.py
"""
DRF Views for Life OS Tracker.

Architecture:
  - ModelViewSet  →  full CRUD (list, create, retrieve, update, partial_update, destroy)
  - Custom @action →  /streak/    per-pillar streak summary
  - Custom @action →  /by-date/   filter logs for a specific date
  - DashboardView  →  single aggregate view across all pillars for today

Authentication: JWT (enforced via DEFAULT_AUTHENTICATION_CLASSES in settings)
Permissions:    IsAuthenticated (all endpoints require a valid token)
"""

