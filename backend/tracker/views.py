class RevisionCardViewSet(viewsets.ModelViewSet):
    queryset = RevisionCard.objects.all().order_by('-created_at')
    serializer_class = RevisionCardSerializer
    permission_classes = [IsAuthenticated]

    @action(detail=True, methods=['post'], url_path='review')
    def mark_reviewed(self, request, pk=None):
        card = self.get_object()
        card.review_count += 1
        card.last_reviewed = timezone.now().date()
        card.save()
        return Response({
            'status': 'reviewed',
            'review_count': card.review_count,
            'last_reviewed': str(card.last_reviewed)
        })


