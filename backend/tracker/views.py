# PILLAR 1: PERFORMANCE
# ══════════════════════════════════════════════════════════════

class PerformanceLogViewSet(PillarViewSetMixin, viewsets.ModelViewSet):
    """
    Full CRUD for Performance (Fitness/Gym) logs.

    Endpoints auto-generated by DRF Router:
      GET    /api/performance/              → list (summary)
      POST   /api/performance/              → create
      GET    /api/performance/<id>/         → retrieve (full)
      PUT    /api/performance/<id>/         → update
      PATCH  /api/performance/<id>/         → partial update
      DELETE /api/performance/<id>/         → destroy

    Custom actions:
      GET    /api/performance/streak/       → streak summary
      GET    /api/performance/by-date/      → logs for a specific date
    """
    queryset                 = PerformanceLog.objects.all().order_by('-date_logged')
    full_serializer_class    = PerformanceLogSerializer
    summary_serializer_class = PerformanceLogSummarySerializer

    filter_backends = [DjangoFilterBackend, filters.OrderingFilter]
    filterset_fields = {
        'date_logged':   ['exact', 'gte', 'lte'],
        'activity_type': ['exact', 'in'],
        'is_rest_day':   ['exact'],
    }
    ordering_fields  = ['date_logged', 'current_streak', 'duration_mins', 'calories_burned']


