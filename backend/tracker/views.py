# DASHBOARD VIEW — Today's snapshot across all pillars
# ══════════════════════════════════════════════════════════════

class DashboardView(APIView):
    """
    GET /api/dashboard/
    Returns today's logs and live streaks for all three pillars.
    Requires authentication.

    Response shape:
    {
        "date": "2026-06-23",
        "performance": [...],
        "study": [...],
        "tech": [...],
        "streaks": {
            "performance": { "current": 5, "longest": 12 },
            "study":       { "current": 3, "longest": 8  },
            "tech":        { "current": 7, "longest": 30 }
        }
    }
    """
    permission_classes = [IsAuthenticated]

    def get(self, request):
        today = date_type.today()

        perf_qs   = PerformanceLog.objects.filter(date_logged=today).order_by('-created_at')
        study_qs  = StudyLog.objects.filter(date_logged=today).order_by('-created_at')
        tech_qs   = TechLog.objects.filter(date_logged=today).order_by('-created_at')
        journal_exists = DailyJournal.objects.filter(date=today).exists()

        def streak_dict(model_class):
            return {
                'current': calculate_streak(model_class),
                'longest': model_class.objects.order_by('-longest_streak')
                           .values_list('longest_streak', flat=True)
                           .first() or 0,
            }

        payload = {
            'date':        str(today),
            'performance': PerformanceLogSummarySerializer(perf_qs, many=True).data,
            'study':       StudyLogSummarySerializer(study_qs, many=True).data,
            'tech':        TechLogSummarySerializer(tech_qs, many=True).data,
            'journal_written': journal_exists,
            'streaks': {
                'performance': streak_dict(PerformanceLog),
                'study':       streak_dict(StudyLog),
                'tech':        streak_dict(TechLog),
            }
        }
        return Response(payload, status=status.HTTP_200_OK)
